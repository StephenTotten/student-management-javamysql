   /**
     * 
     * This is a student class with name and age parameters
     * 
     * @param name
     * 
     * @param age
     *
     * @param id
     */
public class Student {
    private String name;
    private int age;
    private int id;

    // Getter method for 'name' attribute
    public String getName() {
        return name;
    }

    // Setter method for 'name' attribute
    public void setName(String name) {
        this.name = name;
    }

    // Getter method for 'age' attribute
    public int getAge() {
        return age;
    }

    // Setter method for 'age' attribute
    public void setAge(int age) {
        this.age = age;
    }

    // Getter method for 'id' attribute
    public int getId() {
        return id;
    }

    // Setter method for 'id' attribute
    public void setId(int id) {
        this.id = id;
    }
}
